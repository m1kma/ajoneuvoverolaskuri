function calculate() {
    const radioKayttovoimavero09 = document.getElementById('radio-09');
    const radioKayttovoimavero55 = document.getElementById('radio-55');
    const selectedKayttovoimavero = radioKayttovoimavero09.checked ? 0.9 : 5.5;
    const seisontapaivat = document.getElementById('kayttopaivat-value');
    const kayttovoimavero = document.getElementById('result-kayttovoimavero-value');
    const radioNedc = document.getElementById('radio-nedc');
    const radioWtlp = document.getElementById('radio-wltp');
    const radioMassa = document.getElementById('radio-massa');
    const perusvero = document.getElementById('result-perusvero-value');
    const verotyht = document.getElementById('result-verotyht-value');
    const verotyhtpaiva = document.getElementById('result-verotyht-paiva-value');
    const kaytostapoistojenkustannus = document.getElementById('result-kaytostapoistojenkustannus-value');
    const kaskovakuutus = document.getElementById('result-kaskovakuutus-value');
    const liikennevakuutus = document.getElementById('result-liikennevakuutus-value');
    const vakuutuksetyhteensa = document.getElementById('result-vakuutuksetyhteensa-value');
    const kaikkiyhteensa = document.getElementById('result-kaikkiyhteensa-value');
    const kaikkiyhteensaSticky = document.getElementById('result-kaikkiyhteensa-value-sticky');
    const paastotPrefix = document.getElementById('paastot-slider-prefix');
    const radioKasko25Alennus= document.getElementById('radio-25-alennus');
    const radioKasko50alennus = document.getElementById('radio-50-alennus');
    const radioKasko75alennus = document.getElementById('radio-75-alennus');
    const liikennekayttovahintaan = document.getElementById('result-liikennekayttovahintaan-value');


    // Käyttövoimavero
    // (B3/100)*(B4/100)*B5
    // (kokonaismassa * käyttöpäivät * 5,5c)
    const seisontapaivatVal = seisontapaivatSlider.value;
    const kayttopaivatVal = 365 - seisontapaivatSlider.value;
    kayttovoimavero.textContent = parseInt((kokonaismassaSlider.value / 100) * (kayttopaivatVal / 100) * selectedKayttovoimavero);
    seisontapaivat.textContent = kayttopaivatVal;

    // Perusvero
    // G8/365*D5
    // käyttöpäivät / 365 * perusvero vuodessa
    // Verotaulukossa 3401kg ylittävä massa lasketaan samaan arvoon
    const perusveroTaulukosta = radioNedc.checked ? nedc2024TaxTable[paastotSlider.value]
        : radioWtlp.checked ? wltp2024TaxTable[paastotSlider.value]
            : massa2023TaxTable[kokonaismassaSlider.value <= 3400 ? kokonaismassaSlider.value : 3401];

    paastotSlider.disabled = radioMassa.checked;
    paastotPrefix.textContent = paastotSlider.value >= 400 ? ">" : "";
    perusvero.textContent = parseInt(perusveroTaulukosta / 365 * kayttopaivatVal);

    // Verot yhteensä
    const verotyhtValue = parseInt(kayttovoimavero.textContent) + parseInt(perusvero.textContent);
    verotyht.textContent = verotyhtValue;
    verotyhtpaiva.textContent = (verotyhtValue / kayttopaivatVal).toFixed(2);
    liikennekayttovahintaan.textContent = Math.round(10 / verotyhtpaiva.textContent); // kustannus vähintään 10€/päivä

    // Käytöstäpoistojen kustannus
    // käytöstäpoistojen lukumäärä * käytöstäpoiston hinta
    kaytostapoistojenkustannus.textContent = parseInt(kaytostapoistotSlider.value * 6);

    // Kaskovakutus
    // (kaskohinta/365*käytössä)+(kaskohinta/365*seisonnassa)/2
    // (käyttöpäiviltä täyshinta ja seisontapäiviltä puolikas hinta)
    const kaskoAlennus = radioKasko25Alennus.checked ? 0.75 : radioKasko50alennus.checked ? 0.50 : radioKasko75alennus.checked ? 0.25 : 0;
    const kayttopaivienKustannus = kaskovakuutusSlider.value / 365 * kayttopaivatVal;
    const seisontapaivienKustannus = kaskovakuutusSlider.value / 365 * seisontapaivatVal;
    kaskovakuutus.textContent = parseInt(kayttopaivienKustannus + seisontapaivienKustannus * kaskoAlennus);

    // Liikennevakuutus
    // liikennevakuutushinta/365*kayttöpäiviä
    liikennevakuutus.textContent = parseInt(liikennevakuutusSlider.value / 365 * kayttopaivatVal);
    vakuutuksetyhteensa.textContent = parseInt(kaskovakuutus.textContent) + parseInt(liikennevakuutus.textContent);

    const kaikkiyhteensaValue = parseInt(verotyht.textContent) + parseInt(kaytostapoistojenkustannus.textContent) + parseInt(vakuutuksetyhteensa.textContent);
    kaikkiyhteensa.textContent = kaikkiyhteensaValue;
    kaikkiyhteensaSticky.textContent = kaikkiyhteensaValue;

}

